function log(poruka){
  console.log("%c" + poruka, "background: green; color: white; padding: 3px;");
}
function error(poruka){
  console.log("%c" + poruka, "background: red; color: white; padding: 3px;");
}
function warn(poruka){
  console.log("%c" + poruka, "background: yellow; color: black; padding: 3px;");
}